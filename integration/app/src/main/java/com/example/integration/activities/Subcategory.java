package com.example.integration.activities;

public class Subcategory {
    private int sub_category_id;
    private String sub_category_name;

    // Getter for sub_category_id
    public int getSubCategoryId() {
        return sub_category_id;
    }

    // Setter for sub_category_id
    public void setSubCategoryId(int sub_category_id) {
        this.sub_category_id = sub_category_id;
    }

    // Getter for sub_category_name
    public String getSubCategoryName() {
        return sub_category_name;
    }

    // Setter for sub_category_name
    public void setSubCategoryName(String sub_category_name) {
        this.sub_category_name = sub_category_name;
    }
}
